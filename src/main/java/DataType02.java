public class DataType02 {
    public static void main(String[] args) {
        //문자열 타입 데이터
        char c = 'c'; //char = 한 글자의 데이터만 선언이 가능
        String s = "str"; //string = 여러 글자의 데이터 선언이 가능
        byte b = 's'; //byte = 알파벳의 ascii code값 반환

        //논리형 타입 데이터
        boolean t = true; //boolean = true , flase 두개의 논리값을 객체에 할당이 가능함 일반적으로 if,for 문과 함깨 사용
        boolean f = false;

        System.out.println(t);
        System.out.println(f);
    }
}
